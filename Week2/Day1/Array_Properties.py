# Number of Dimensions (ndim)
import numpy as np
Array=np.array([10,20,30,40,50])
print(Array.ndim)

# Structure of Array (Shape) 
print(Array.shape)

# Total Elements (Size)
print(Array.size)

# Data Type (dtype)
print(Array.dtype)

# Array filled with Zeros (np.zeros())
array=np.zeros(5)
print(array)


# Array filled with ones (np.ones())
array=np.ones(5)
print(array)

# (np.arange(start, stop, step))
array=np.arange(0, 10, 2)
print(array)